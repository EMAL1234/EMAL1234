  $(document).ready(function(){
    $("#cls").on({
        mouseenter: function(){
            $(this).css("background-color", "#C0C0C0");
        },  
       
        mouseleave: function(){
            $(this).css("background-color", "white");
        },
       
        dblclick: function(){
            $(this).css("background-color", "yellow");
        }
    });
});
